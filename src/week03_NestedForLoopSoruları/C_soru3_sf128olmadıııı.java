package week03_NestedForLoopSoruları;

public class C_soru3_sf128olmadıııı {
    public static void main(String[] args) {

       //  * * * * * * * *
       //  * * * * * *
       //  * * * *
       //  * *     şeklinde yazdır

        for (int i = 4 ; i <=1 ; i--) {
            for (int j = 1; j <=i*2 ; j++) {
                System.out.print("* ");
            }
            System.out.println(" ");
        }


    }
}
