package week03_NestedForLoopSoruları;

public class C_soru2_sf128 {
    public static void main(String[] args) {

        // ******** 4 x 8 lik * grubu yazdır

        for (int i = 1; i <=4 ; i++) {
            for (int j = 1; j <=8 ; j++) {
                System.out.print("* ");
            }
            System.out.println(" ");
        }

    }
}
