package week02_forLoop;

public class C_soru1 {
    public static void main(String[] args) {

        // soru1 : 1 den 100e kadar sayıları aynı satırda aralarında bir boşluk bırakarak yazdırın



        for ( int  sayi = 1 ; sayi <= 100 ; sayi ++  ) {

            System.out.print(sayi + " ");}
    }
}
